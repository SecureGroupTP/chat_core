//! Ядро MLS-мессенджера с Rust API и C FFI фасадом.
//!
//! Крейт включает:
//! - сервисный API [`MessengerMls`] для оркестрации на уровне приложения;
//! - backend-реализацию на основе OpenMLS (модуль `backend`);
//! - сериализуемые DTO для состояния и FFI-транспорта (модуль `types`);
//! - C ABI точки входа для SDK на разных языках (модуль `ffi`).

#![warn(missing_docs)]

/// Абстракции OpenMLS backend и реализация по умолчанию.
pub mod backend;
/// C ABI фасад поверх [`MessengerMls`].
pub mod ffi;
/// Высокоуровневый сервисный API для жизненного цикла MLS-клиента и работы с группами.
pub mod service;
/// Модели состояния в памяти и в сериализованном виде для сервисного слоя.
pub mod state;
/// Публичные DTO, коды статусов и алиасы ошибок/результатов.
pub mod types;

/// Основной высокоуровневый MLS-сервис.
pub use service::MessengerMls;
/// Переэкспорт публичных API-типов.
pub use types::*;
